// 旧制度マスタ生成用ヘルパー
// type: 1(～H11.3), 2(H11.4～H12.3), 3(H12.4～H23.9)
const generateOldSystemTable = (type: 1 | 2 | 3): TableRowT1[] => {
    const rows: TableRowT1[] = [];
    let accLos = 0, accR1 = 0;
    
    // マスタは47年まで作成 (十分な長さ)
    for (let y = 1; y <= 47; y++) {
        let losUnit = 0;

        // --- 勤続ポイント定義 (Los) ---
        if (type === 1) {
            // Type 1: ① 平成11年3月31日以前
            if (y <= 6) losUnit = 5;
            else if (y <= 11) losUnit = 10;
            else if (y <= 16) losUnit = 25;
            else if (y <= 21) losUnit = 45;
            else if (y <= 25) losUnit = 50; 
            else if (y <= 30) losUnit = 40; 
            else if (y <= 35) losUnit = 35; 
            else losUnit = 0;               
        } else if (type === 2) {
            // Type 2: ② 平成11年4月1日〜平成12年3月31日
            if (y < 2) losUnit = 0; // 満1年未満相当期間（待機）
            else if (y <= 7) losUnit = 5;
            else if (y <= 12) losUnit = 10;
            else if (y <= 17) losUnit = 25;
            else if (y <= 22) losUnit = 45;
            else if (y <= 26) losUnit = 50; 
            else if (y <= 31) losUnit = 40; 
            else if (y <= 36) losUnit = 35; 
            else losUnit = 0;               
        } else {
            // Type 3: ③ 平成12年4月1日〜平成23年9月30日
            // 勤続Pt特例: 初期3年間(y1-3)は0pt
            if (y <= 3) losUnit = 0;
            else if (y <= 6) losUnit = 5;   
            else if (y <= 11) losUnit = 10; 
            else if (y <= 16) losUnit = 25;
            else if (y <= 21) losUnit = 45;
            else if (y <= 25) losUnit = 50; 
            else if (y <= 30) losUnit = 40; 
            else if (y <= 35) losUnit = 35; 
            else losUnit = 0;               
        }
        accLos += losUnit;

        // --- 職能ポイント定義 (Rank 1等級) ---
        let r1Unit = 0;
        
        if (type === 1) {
            // 旧制度①
            // 1号: 満5年未満 (y1-4) -> 1
            // 2号: 満5年以上 満13年未満 (y5-12) -> 2
            // ...
            if (y <= 4) r1Unit = 1;
            else if (y <= 12) r1Unit = 2;
            else if (y <= 19) r1Unit = 3;
            else if (y === 20) r1Unit = 4;
            else if (y <= 28) r1Unit = 5;
            else if (y <= 30) r1Unit = 6;
            else if (y <= 35) r1Unit = 7;
            else r1Unit = 0;
        } else if (type === 2) {
            // 旧制度② (修正: 1年目は0pt)
            // 1号: 満1年以上 満6年未満 (y2-6) -> 1
            // 2号: 満6年以上 満14年未満 (y7-14) -> 2
            // 3号: 満14年以上 満21年未満 (y15-21) -> 3
            // 4号: 満21年以上 満22年未満 (y22) -> 4
            // 5号: 満22年以上 満30年未満 (y23-30) -> 5
            // 6号: 満30年以上 満32年未満 (y31-32) -> 6
            // 7号: 満32年以上 (y33-36) -> 7
            if (y < 2) r1Unit = 0;
            else if (y <= 6) r1Unit = 1;
            else if (y <= 14) r1Unit = 2;
            else if (y <= 21) r1Unit = 3;
            else if (y === 22) r1Unit = 4;
            else if (y <= 30) r1Unit = 5;
            else if (y <= 32) r1Unit = 6;
            else if (y <= 36) r1Unit = 7;
            else r1Unit = 0;
        } else {
            // 旧制度③
            if (y < 2) r1Unit = 0;
            else if (y <= 6) r1Unit = 1;
            else if (y <= 14) r1Unit = 2;
            else if (y <= 21) r1Unit = 3;
            else if (y === 22) r1Unit = 4;
            else if (y <= 30) r1Unit = 5;
            else if (y <= 32) r1Unit = 6;
            else if (y <= 37) r1Unit = 7;
            else r1Unit = 0;
        }
        accR1 += r1Unit;

        // --- 職能ポイント定義 (Rank 2等級以上) ---
        // 在級年数満1年につき X点 (累積計算)
        // 修正: Type 3 も待機なしで計算 (y-1処理を削除済み)
        
        let effectiveY = 0;
        if (type === 1) {
            effectiveY = Math.min(y, 35);
        } else if (type === 2) {
            effectiveY = Math.min(y, 36);
        } else {
            // Type 3: Max 37
            effectiveY = Math.min(y, 37); 
        }

        rows.push({ 
            y, 
            los1: accLos, 
            r1_1: accR1, 
            r2: effectiveY * 9, 
            r3: effectiveY * 11, 
            r4: effectiveY * 21, 
            r5: effectiveY * 26, 
            r6: effectiveY * 31 
        });
    }
    return rows;
};

